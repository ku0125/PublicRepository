from flask import Flask

# ==================================================
# インスタンス生成
# ==================================================
app = Flask(__name__)


# ==================================================
# ルーティング
# ==================================================
@app.route("/")
def hello_world():
    return "<h1>ハローワールド</h1>"

# 実行ボタンで動作させる
if __name__ == "__main__":
    app.run(host="0.0.0.0", port=5001, debug=True)
